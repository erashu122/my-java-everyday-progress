public class operator {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(a);
        System.out.println(!a);
        // The output will be:
        // true 
        // false
        // Explanation: The '!' operator inverts the boolean value.
        // In this case, it changes 'true' to 'false'.
        int b= 10;
        System.out.println(~b);

        int c= -5;
        System.out.println(~c);
;
    }
}